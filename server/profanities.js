profanitiesList.forEach(function (word, i) {
	// will not add a string if it is a superstring of another word
	// for example, "shite" will not be added if "shit" is in the list
	if (profanitiesList.every(function(otherWord, j) { return i === j || word.search(otherWord) === -1; })) {
		var chars = word.split('');
		var cur = profanities;

		for (var i = 0, _len = word.length; i < _len; i++) {
			cur = cur[chars[i]] = i === _len - 1 ? true : cur[chars[i]] || {};
		}
	}
});

